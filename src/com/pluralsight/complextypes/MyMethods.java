package com.pluralsight.complextypes;

public class MyMethods {

    public static void swap(int i, int j) {
        int k = i;
        i = j;
        j = k;
        System.out.print("Inside Swap i:  ");
        System.out.println(i);
        System.out.print("Inside Swap j:  ");
        System.out.println(j);
    }


}
